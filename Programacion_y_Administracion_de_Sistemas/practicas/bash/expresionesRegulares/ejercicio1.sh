#!/bin/bash

echo ""

#CONTROL DE ARGUMENTOS
#---------------------------------

if [ $# -ne 1 ]
then
	echo -e "Este programa solo requiere un argumento, el cual es un directorio (peliculas.txt).\n"
	exit 1
fi

#---------------------------------


#GREP1
#---------------------------------

cat $1 | grep --colour "[1-9]h [0-9][0-9]min"

#---------------------------------

echo ""

#GREP2
#---------------------------------

cat $1 | grep --colour "\[.*\]"

#---------------------------------

echo ""

#GREP3
#---------------------------------

cat $1 | grep -o --colour "\[.*\]"

#---------------------------------

echo ""

#GREP4
#---------------------------------

let cont2016=$(cat $1 | grep -E -c "2016")
let cont2017=$(cat $1 | grep -E -c "2017")
echo "Hay $cont2016 de 2016 y $cont2017 de 2017."

#---------------------------------

echo ""


#GREP5
#---------------------------------

echo -e "Eliminar lineas vacias:\n"
cat $1 | grep -v "^$"

#---------------------------------

echo""

#GREP6
#---------------------------------

cat $1 | grep -E --colour "^ *(E|L)"

#---------------------------------

echo ""

#GREP7
#---------------------------------

cat $1 | grep -E --colour "((d[aeiou]d)|(t[aeiou]t)|(l[aeiou]l))"

#---------------------------------

echo ""

#GREP8
#---------------------------------

cat $1 | grep -E --colour "(.*a){8,}"

#---------------------------------

echo ""

#GREP9
#---------------------------------

cat $1 | grep -i -E --colour "^[^ ].*\.{3}$"

#---------------------------------

echo ""


#GREP10
#---------------------------------

cat $1 | sed -r -e 's/([áéíóú])/"\1"/gI' | grep --colour -i "\"[áéíóú]\""

#---------------------------------

echo ""
